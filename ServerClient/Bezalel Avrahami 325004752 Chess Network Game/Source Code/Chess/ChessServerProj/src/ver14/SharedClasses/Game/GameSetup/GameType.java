package ver14.SharedClasses.Game.GameSetup;

/**
 * represents a Game's type.
 *
 * @author Bezalel Avrahami (bezalel3250@gmail.com)
 */
public enum GameType {
    /**
     * Join existing game.
     */
    JOIN_EXISTING,
    /**
     * Resume game.
     */
    RESUME,
    /**
     * Create new game.
     */
    CREATE_NEW,
    /**
     * Quick match.
     */
    QUICK_MATCH;

}
